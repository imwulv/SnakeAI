    public void reset(){
        for (pers.wulv.model.Snake snake : snakeList) {
            snake.reset();
        }
    }
    public void paint(Graphics g) {

        //绘制基础地图
        this.setBackground(Color.BLACK);
        g.setColor(Color.GRAY);
        for (int i = 0; i < orginMapList.length; i++) {
            for (int j = 0; j < orginMapList[i].length; j++) {
                switch (orginMapList[i][j]){
                    //遮挡物，触之即死
                    case 1:
                        g.setColor(Color.GRAY);
                        break;
                    //可用空间
                    case 0:
                        g.setColor( Color.lightGray);
                        break;
                }
                g.fillRect(rectLength * j, rectLength * i, rectLength, rectLength);
            }
        }

        int i = 1;
        for (pers.wulv.model.Snake snake : snakeList) {

            //绘制Snake
            for (SnakeNode snakeNode : snake.snakeNodes) {
                g.setColor(snake.snakeColor);
                g.fillRect(rectLength * snakeNode.x, rectLength * snakeNode.y, rectLength, rectLength);
            }
            //绘制食物
            g.setColor(snake.foodColor);
            g.fillRect(rectLength * snake.food.x, rectLength * snake.food.y, rectLength, rectLength);

            //绘制分数信息
            g.setColor(Color.WHITE);
            g.setFont(new Font("arial", Font.BOLD, 25));
            g.drawString("Snake "+ i +" Score: " + snake.score +" LifeTime: "+ snake.lifeTime+ " Death : " + snake.isFaild, 620, 30 * i++);
        }
        super.paintChildren(g);
    }

